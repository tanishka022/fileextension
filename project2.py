fname = input("Input the Filename: ")

if ".py" in fname:
    print("The extension of the file is: python")

elif ".txt" in fname:
    print("The extension of the file is: plain text file")

elif ".bin" in fname:
    print("The extension of the file is: binary file")

elif ".dat" in fname:
    print("The extension of the file is: data file")

elif ".db" in fname:
    print("The extension of the file is: database file")

elif ".sql" in fname:
    print("The extension of the file is: SQL database file")

elif ".png" in fname:
    print("The extension of the file is: png image")

elif ".pdf" in fname:
    print("The extension of the file is: PDF file")

elif ".doc" in fname:
    print("The extension of the file is: Microsoft Word file")